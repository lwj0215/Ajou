INLP  TD    INDEV .INPUT
      JEQ   INLP
	LDX   #0
RDNUM	CLEAR	A
	RD	INDEV
	COMP	#48	.0'S ASCII CODE IS 48
	JLT	STCNT
	COMP	#57
	JGT	STCNT
	SUB	#48
	STCH	DATA,X	.USE STCH FOR STORE 1BYTE
	TIX	#0
	J	RDNUM
STCNT	STX	CNT

	LDA	#0
	LDX	#0
	STA	BDATA
CONV	LDA	#0
	LDCH	DATA,X
	STA	TMP
	LDA	BDATA
	MUL	#10
	ADD	TMP
	STA	BDATA
	TIX	CNT
	JLT	CONV

OUTLP	TD	OUTDEV	.OUTPUT
	JEQ	OUTLP
	LDX	#1
WRNUM	CLEAR	A
	LDA	BDATA
	AND	MASK
	COMP	#0
	JEQ	OZERO
	CLEAR	A
	LDCH	ONE	.LOAD ONE
	J	WCHAR
OZERO	LDCH	ZERO	.LOAD ZERO
WCHAR	WD	OUTDEV	.WRITE CHAR
	LDA	MASK
	SHIFTR	A,1
	AND	LMASK
	STA	MASK
	STX	TMP
	CLEAR	A
	LDA	TMP
	AND	#7
	COMP	#0
	JGT	PSSP
	CLEAR	A
	LDCH	SPACE
	WD	OUTDEV	.WRITE SPACE
PSSP	STX	TMP	.PASS SPACE
	TIX	MAXLEN
	JLT	WRNUM
HALT	J	HALT

INDEV	BYTE	0x03
OUTDEV	BYTE	0x04
DATA	RESB	10
CNT	WORD	0
BDATA	RESW	1
RESULT	RESB	3
TMP	RESW	1
MASK	WORD	X'800000'
ZERO	BYTE	C'0'
ONE	BYTE	C'1'
SPACE	BYTE	C' '
MAXLEN	WORD	25
LMASK	WORD	X'7FFFFF'
